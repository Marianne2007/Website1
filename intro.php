           and religious and community (hate crime) settings. It touches victims 
           across the lifespan from children through elders. Abuse is a serious 
           social and cultural problem affecting everyone whether as a victim of 
           abuse, a perpetrator, a friend or confidant of an abused person looking 
           for ways to be helpful, or simply as someone who is angered by injustice 
           and wants to work for positive change.
